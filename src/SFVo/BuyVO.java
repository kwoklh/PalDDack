package SFVo;

// 구매 VO
public class BuyVO {

	private String buyNo; // 구매번호
	private String buyWay; // 결재방법
	
	public String getBuyNo() {
		return buyNo;
	}
	public void setBuyNo(String buyNo) {
		this.buyNo = buyNo;
	}
	public String getBuyWay() {
		return buyWay;
	}
	public void setBuyWay(String buyWay) {
		this.buyWay = buyWay;
	}
	
}
