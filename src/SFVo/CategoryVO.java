package SFVo;

// 카테고리 VO
public class CategoryVO {
	private String cgCode; // 카테고리분류코드
	private String cgName; // 카테고리명
	
	public String getCgCode() {
		return cgCode;
	}
	public void setCgCode(String cgCode) {
		this.cgCode = cgCode;
	}
	public String getCgName() {
		return cgName;
	}
	public void setCgName(String cgName) {
		this.cgName = cgName;
	}
	
}
